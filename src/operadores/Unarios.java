package operadores;

public class Unarios {

    public static void main(String[] args) {

        int a = 1;
        int b = 2;

        // POS FIX ( PÃ³s Fixada)
        a++; // a = a + 1;
        a--; // a = a - 1;

        ++b; // b = b + 1;
        --b; // b = b - 1;
        System.out.println(a);
        System.out.println(b);


    }
}
